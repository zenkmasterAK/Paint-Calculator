public class Triplet<T> {
    private T first;
    private T second;
    private T third;

    public T getFirst() {
        return first;
    }

    public void setFirst(T first) {
        this.first = first;
    }

    public T getSecond() {
        return second;
    }

    public void setSecond(T second) {
        this.second = second;
    }

    public T getThird() {
        return third;
    }

    public void setThird(T third) {
        this.third = third;
    }
  
    
    Triplet<Double> dimensions=new Triplet<Double>();
   
}
